const PLACEHOLDER_PALETTE: Record<string, string> = {
  "0": "rgba(0,0,0,0)",
  "1": "#555555",
  "2": "#8B4513",
  "3": "#CC0000",
  "4": "#FF8800",
  "5": "#FFDD00",
  "6": "#00AA00",
  "7": "#3366CC",
  "8": "#8833AA",
  "9": "#FFFFFF",
};

const SCALE = 4;

function drawSprite(
  ctx: CanvasRenderingContext2D,
  csv: string,
  palette: Record<string, string>
) {
  const rows = csv.trim().split("\n");
  for (let y = 0; y < rows.length; y++) {
    const cols = rows[y].split(",");
    for (let x = 0; x < cols.length; x++) {
      const color = palette[cols[x]];
      if (!color || cols[x] === "0") continue;
      ctx.fillStyle = color;
      ctx.fillRect(x * SCALE, y * SCALE, SCALE, SCALE);
    }
  }
}

export default function Home() {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [description, setDescription] = useState("");
  const [sprite, setSprite] = useState<SpriteResult | null>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  // Draw whenever sprite changes
  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    ctx.clearRect(0, 0, 256, 256);

    if (sprite) {
      drawSprite(ctx, sprite.csv, sprite.palette);
    } else {
      drawSprite(ctx, PLACEHOLDER_CSV, PLACEHOLDER_PALETTE);
    }
  }, [sprite]);

  const generate = useCallback(async () => {
    if (!description.trim()) return;
    setLoading(true);
    setError(null);
    try {
      const res = await fetch("/api/generate", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ description: description.trim() }),
      });
      const data = await res.json();
      if (!res.ok) throw new Error(data.error || "Generation failed");
      setSprite(data);
    } catch (err) {
      setError(err instanceof Error ? err.message : "Unknown error");
    } finally {
      setLoading(false);
    }
  }, [description]);

  const handleKeyDown = useCallback(
    (e: React.KeyboardEvent) => {
      if (e.key === "Enter" && !loading) generate();
    },
    [generate, loading]
  );

  return (
    <div className="min-h-screen bg-zinc-900 flex flex-col items-center justify-center gap-6 px-4">
      <canvas
        ref={canvasRef}
        width={256}
        height={256}
        className="border border-zinc-700 rounded"
      />

      <div className="flex gap-2 w-full max-w-sm">
        <input
          type="text"
          value={description}
          onChange={(e) => setDescription(e.target.value)}
          onKeyDown={handleKeyDown}
          placeholder="a red heart, a blue sword, a green tree..."
          disabled={loading}
          className="flex-1 px-3 py-2 rounded bg-zinc-800 border border-zinc-600 text-zinc-100 placeholder:text-zinc-500 focus:outline-none focus:border-zinc-400 disabled:opacity-50"
        />
        <button
          onClick={generate}
          disabled={loading || !description.trim()}
          className="px-4 py-2 rounded bg-zinc-100 text-zinc-900 font-medium hover:bg-zinc-200 disabled:opacity-50 disabled:cursor-not-allowed"
        >
          {loading ? "..." : "Generate"}
        </button>
      </div>

      {error && (
        <p className="text-red-400 text-sm max-w-sm text-center">{error}</p>
      )}

      {sprite && !loading && (
        <>
          <div className="flex flex-wrap gap-2 justify-center max-w-sm">
            {Object.entries(sprite.palette).map(([index, color]) => (
              <div key={index} className="flex items-center gap-1 text-xs text-zinc-400">
                <span
                  className="inline-block w-4 h-4 rounded border border-zinc-600"
                  style={{ backgroundColor: color }}
                />
                {index}
              </div>
            ))}
          </div>
          <p className="text-xs text-zinc-500">
            {sprite.usage.inputTokens} in + {sprite.usage.outputTokens} out = {sprite.usage.totalTokens} tokens · {sprite.usage.costUSD}
          </p>
        </>
      )}
    </div>
  );
}
